const escapeSql = (value: string): string => value.replaceAll("'", "''").replaceAll("\n", "\\n");

// public/avatarsに生成済み画像があればavatarパスを返す
const resolveAvatar = (id: string, explicit: string | null): string | null => {
  if (explicit) return explicit;
  const avatarFile = join(import.meta.dirname, "..", "public", "avatars", `${id}.jpg`);
  try {
    return existsSync(avatarFile) ? `/avatars/${id}.jpg` : null;
  } catch {
    return null;
  }
};

const generateSql = (): string => {
  const lines: string[] = [];

  lines.push(
    `INSERT OR REPLACE INTO user (id, email, created_at) VALUES ('${USER_ID}', '${USER_EMAIL}', ${ts(30)});`,
  );

  for (const c of characters) {
    const avatar = resolveAvatar(c.id, c.avatar);
    lines.push(
      `INSERT OR REPLACE INTO character (id, user_id, name, avatar, system_prompt, greeting, tags, created_at) VALUES ('${c.id}', '${USER_ID}', '${escapeSql(c.name)}', ${avatar === null ? "NULL" : `'${escapeSql(avatar)}'`}, '${escapeSql(c.systemPrompt)}', '${escapeSql(c.greeting)}', '${escapeSql(JSON.stringify(c.tags))}', ${ts(10)});`,
    );
  }

  for (const conv of conversations) {
    const convTs = ts(conv.daysAgo);
    const lastMsg = conv.messages.at(-1);
    const updatedAt = lastMsg ? convTs + lastMsg.minutesAfterStart * 60_000 : convTs;

    lines.push(
      `INSERT OR REPLACE INTO conversation (id, user_id, character_id, title, created_at, updated_at) VALUES ('${conv.id}', '${USER_ID}', '${conv.characterId}', '${escapeSql(conv.title)}', ${convTs}, ${updatedAt});`,
    );

    for (const [msgIdx, msg] of conv.messages.entries()) {
      const msgId = generateMessageId(conv.id, msgIdx);
      const msgTs = convTs + msg.minutesAfterStart * 60_000;
      lines.push(
        `INSERT OR REPLACE INTO message (id, user_id, conversation_id, character_id, role, content, created_at) VALUES ('${msgId}', '${USER_ID}', '${conv.id}', '${conv.characterId}', '${msg.role}', '${escapeSql(msg.content)}', ${msgTs});`,
      );
    }
  }

  return lines.join("\n");
};

const RESET_SQL = [
  "DELETE FROM message;",
  "DELETE FROM conversation;",
  "DELETE FROM character;",
  "DELETE FROM user;",
].join("\n");

const isReset = process.argv.includes("--reset");

if (isReset) {
  console.info("🗑️  Resetting local D1 database...");
  execSync(`wrangler d1 execute ${DB_NAME} --local --command="${RESET_SQL}"`, {
    stdio: "inherit",
  });
}

console.info("🌱 Seeding local D1 database...");

const tmpFile = join(import.meta.dirname, ".seed.sql");
writeFileSync(tmpFile, generateSql(), "utf-8");

try {
  execSync(`wrangler d1 execute ${DB_NAME} --local --file="${tmpFile}"`, {
    stdio: "inherit",
  });
  console.info(
    "✅ Seed complete! Characters: %d, Conversations: %d",
    characters.length,
    conversations.length,
  );
} finally {
  unlinkSync(tmpFile);
}
